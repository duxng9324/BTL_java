    private void tblMuonTraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMuonTraMouseClicked
         int SelectedRow = tblMuonTra.getSelectedRow();
         if (SelectedRow >= 0) {
            String maDocGia = (String) tblMuonTra.getValueAt(SelectedRow, 0);
            for (MuonTra m : danhSachMuonTra) {
                if (maDocGia.equals(m.getMaDocGia())) {
                    txtMaDocGia.setText(m.getMaDocGia());
                    txtMaSach.setText(m.getMaSach());
                    txtNgayMuon.setText(m.getNgayMuon());
                    txtNgayTra.setText(m.getNgayTra());
                    break;
                }
            }
        }
    }//GEN-LAST:event_tblMuonTraMouseClicked

    private void btnMuonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMuonActionPerformed
        String maDocGia = txtMaDocGia.getText();
        String maSach = txtMaSach.getText();
        String ngayMuon = txtNgayMuon.getText();
        String ngayTra = txtNgayTra.getText();
        boolean daTra = false; // Mặc định là chưa trả

        MuonTra muonTra = new MuonTra(maDocGia, maSach, ngayMuon, ngayTra, daTra);
        danhSachMuonTra.add(muonTra);
        fillTable();
        
        try (BufferedWriter bw = new BufferedWriter(new FileWriter("CSDL\\MuonTra.txt", false))) {
        for (MuonTra m : danhSachMuonTra) {
            bw.write(m.toString());
            bw.newLine();
        }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi ghi file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        updateSoLuongSach(muonTra.getMaSach());
        saveDanhSachSachToFile();
    }//GEN-LAST:event_btnMuonActionPerformed

    private void btnTraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraActionPerformed
        int selectedRow = tblMuonTra.getSelectedRow();
    if (selectedRow >= 0) {
        String maDocGia = txtMaDocGia.getText();
        String maSach = txtMaSach.getText();

        for (MuonTra m : danhSachMuonTra) {
            if (m.getMaDocGia().equals(maDocGia) && m.getMaSach().equals(maSach)) {
                m.setDaTra(true);
                updateSoLuongSachWhenReturned(maSach);
                fillTable();
                saveDanhSachMuonTraToFile();
                saveDanhSachSachToFile();
                JOptionPane.showMessageDialog(this, "Sách đã được trả thành công!");
                return;
            }
        }
    } else {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn sách để trả!", "Lỗi", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnTraActionPerformed

