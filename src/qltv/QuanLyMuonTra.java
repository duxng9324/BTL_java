    private void fillTable() {
    tblModel.setRowCount(0);

    if (danhSachMuonTra != null && !danhSachMuonTra.isEmpty()) {
        for (MuonTra m : danhSachMuonTra) {
            String trangThai = m.getDaTra() ? "Đã trả" : "Chưa trả";
            tblModel.addRow(new Object[]{m.getMaDocGia(), m.getMaSach(), m.getNgayMuon(), m.getNgayTra(), trangThai});
        }
    }
}
     
     private void initTable() {
        String[] columns = new String[]{"Mã đọc giả", "Mã sách", "Ngày mượn", "Ngày trả", "Trạng thái"};
        tblModel.setColumnIdentifiers(columns);
        tblMuonTra.setModel(tblModel);
    }
private void updateSoLuongSach(String maSach) {
    for (Sach s : danhSachSach) {
        if (s.getMaSach().equals(maSach)) {
            s.setSoLuong(s.getSoLuong() - 1);
            break;
        }
    }
}

private void saveDanhSachSachToFile() {
    try (BufferedWriter bw = new BufferedWriter(new FileWriter("CSDL\\Sach.txt"))) {
        for (Sach s : danhSachSach) {
            bw.write(s.toString());
            bw.newLine();
        }
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi ghi file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
private void updateSoLuongSachWhenReturned(String maSach) {
    for (Sach s : danhSachSach) {
        if (s.getMaSach().equals(maSach)) {
            s.setSoLuong(s.getSoLuong() + 1);
            break;
        }
    }
}

private void saveDanhSachMuonTraToFile() {
    try (BufferedWriter bw = new BufferedWriter(new FileWriter("CSDL\\MuonTra.txt", false))) {
        for (MuonTra m : danhSachMuonTra) {
            bw.write(m.toString());
            bw.newLine();
        }
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi ghi file: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
